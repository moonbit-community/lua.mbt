-- Test bitwise operators
print("Bitwise AND:")
print("12 & 10 =", 12 & 10)

print("Bitwise OR:")
print("12 | 10 =", 12 | 10)

print("Bitwise XOR:")
print("12 ~ 10 =", 12 ~ 10)

print("Bitwise NOT:")
print("~5 =", ~5)

print("Left shift:")
print("1 << 3 =", 1 << 3)
print("5 << 2 =", 5 << 2)

print("Right shift:")
print("8 >> 2 =", 8 >> 2)
print("20 >> 1 =", 20 >> 1)
